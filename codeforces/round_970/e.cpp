#include <bits/stdc++.h>

using namespace std;

#define endl '\n' 
#define int long long

void solve () {
    int n; cin >> n;
    string s; cin >> s;

    string e, o;
    for (int i = 0; i < n; i++) {
        if (i&1) o.push_back(s[i]);
        else e.push_back(s[i]);
    }

    cout << "e -> " << e << endl;
    cout << "o -> " << o << endl;
}

signed main() {
    ios_base::sync_with_stdio(0);cin.tie(0);
    int TC = 1;
    if (TC){
        cin >> TC;
        while (TC--) solve();
    } else {
        solve();
    }
    return 0;
}

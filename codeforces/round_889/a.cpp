void solve () {
    int n; cin >> n;
    int diff = 0;
    int resp = 0;
    for (int i=1; i <= n; i++) {
        int num; cin >> num;
        if (num == i) diff++;
    }
    resp += diff/2;
    resp += diff % 2;
    cout << resp << endl;
}

signed main() {
    ios_base::sync_with_stdio(0);cin.tie(0);
    int TC = 1;
    if (TC){
        cin >> TC;
        while (TC--) solve();
    } else {
        solve();
    }
    return 0;
}

void solve () {
    string s;
    getline(std::cin, s);

    stringstream ss(s);

    string resp;
    string n;

    while (ss >> n) {
        if (n.size() > resp.size()) resp = n;
    }

    cout << resp << endl;
}

signed main() {
    ios_base::sync_with_stdio(0);cin.tie(0);
    int TC = 0;
    if (TC){
        cin >> TC;
        while (TC--) solve();
    } else {
        solve();
    }
    return 0;
}
